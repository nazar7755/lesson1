# --- Функції для ролі стартапера ---
def startupper_menu():
    while True:
        print("\nМеню стартапера:")
        print("1. Переглянути список проектів")  # Перегляд доступних проектів
        print("2. Додати новий проект")  # Додавання нового проекту
        print("3. Повернутись до головного меню")  # Вихід у головне меню
        choice = input("Оберіть дію (1-3): ")

        if choice == "1":
            show_projects()  # Функція для перегляду проектів
        elif choice == "2":
            add_project()  # Функція для додавання нового проекту
        elif choice == "3":
            break  # Вихід із меню стартапера
        else:
            print("Невірний вибір, спробуйте ще раз.")  # Якщо користувач вибрав неправильний варіант

# Додавання нового проекту.
# Ми питаємо у користувача ім'я, опис, вартість і потреби для нового проекту.
def add_project():
    name = input("Введіть назву проекту: ")  # Назва проекту
    description = input("Опис проекту: ")  # Опис проекту
    value = float(input("Оцініть вартість проекту (в $): "))  # Вартість проекту
    needs = input("Введіть список ресурсів через кому: ").split(",")  # Ресурси для проекту (розділяємо через кому)
    projects.append({
        "name": name,
        "description": description,
        "value": value,
        "needs": [need.strip() for need in needs],  # Прибираємо зайві пробіли
        "investor": None  # Поки що інвестора немає
    })
    print(f"Проект '{name}' додано до списку!")  # Підтвердження додавання проекту

# Функція для перегляду всіх проектів.
# Якщо проектів немає, виводимо відповідне повідомлення.
def show_projects():
    if not projects:
        print("Поки що проектів немає.")  # Якщо список порожній
    else:
        print("\nСписок проектів:")
        for i, project in enumerate(projects, 1):  # Перегляд кожного проекту з нумерацією
            print(f"{i}. {project['name']} (вартість: ${project['value']:.2f})")

