# Тут ми виводимо додаткову інформацію, як опис, вартість і потреби.
def show_projects_with_details():
    if not projects:
        print("Немає доступних проектів.")  # Якщо проектів немає
    else:
        print("\nДеталі проектів:")
        for i, project in enumerate(projects, 1):
            print(f"{i}. {project['name']}")
            print(f"   Опис: {project['description']}")
            print(f"   Вартість: ${project['value']:.2f}")
            print(f"   Потреби: {', '.join(project['needs'])}")  # Перелік потреб
            if project["investor"]:
                print(f"   Інвестор: {project['investor']['name']}, Частка: {project['investor']['shares']}%")
            else:
                print("   Інвестор: Поки що немає")

# Функція для інвестування в проект.
# Якщо проект ще не має інвестора, можна вкласти гроші та отримати частку.
def invest_in_project():
    show_projects_with_details()  # Показуємо деталі проектів
    if not projects:
        return

    try:
        # Обираємо проект за номером
        choice = int(input("Виберіть номер проекту для інвестування: "))
        project = projects[choice - 1]
    except (ValueError, IndexError):
        print("Невірний вибір. Спробуйте ще раз.")
        return

    if project["investor"]:
        print("Цей проект вже має інвестора.")  # Якщо проект вже має інвестора
        return

    name = input("Ваше ім'я: ")  # Ім'я інвестора
    budget = float(input("Скільки ви готові вкласти ($): "))  # Сума для інвестування

    if budget < project["value"]:
        print("Ваш бюджет недостатній для цього проекту.")  # Якщо сума менша за вартість проекту
        return

    # Розрахунок частки інвестора в проекті
    shares = (project["value"] / budget) * 100
    project["investor"] = {"name": name, "shares": shares}  # Додаємо інвестора до проекту
    print(f"Ви успішно інвестували у проект '{project['name']}'. Ваша частка: {shares:.2f}%")

